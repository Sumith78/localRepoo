public class PrintFriendsNames {
    public static void printFriendsNames(String friend1, String friend2, String friend3) {
        System.out.println("Hi " + friend3 + ", " + friend2 + " and " + friend1);
    }

    public static void main(String[] args) {
        String friend1 = "Mahesh";
        String friend2 = "Suresh";
        String friend3 = "Devesh";
        
        printFriendsNames(friend1, friend2, friend3);
    }
}


